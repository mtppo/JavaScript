// Utilizando Arrow Functions

// declaração de funcção do Arrow
var valor = (nome) => {
    // corpo da função
    return `Seja bem-vindo: ${nome}`;
}

console.log(valor);

var boasVindas = nome => {
    console.log("Seja bem-vindo, " + nome);
}

boasVindas("Marcio");

